def plotly_price_calculator():
    st.title("üìä –°—Ä–∞–≤–Ω–µ–Ω–∏–µ —Ü–µ–Ω –Ω–∞ —Ç–æ–≤–∞—Ä—ã")
    
    prices = pd.read_csv('./final_data/prices.csv')

    prices['date'] = pd.to_datetime(prices['date'])

    col1, col2 = st.columns(2)
    with col1:
    # –í—ã–±–æ—Ä —Ç–æ–≤–∞—Ä–∞
        product = st.selectbox("–í—ã–±–µ—Ä–∏—Ç–µ —Ç–æ–≤–∞—Ä:", list(prices['product'].unique()))
    
    with col2:
    # –í–≤–æ–¥ —Ü–µ–Ω—ã
        current_price = st.number_input("–í–∞—à–∞ —Ü–µ–Ω–∞ (—Ä—É–±):", min_value=0.0, value=475.0, step=1.0)

    # –§–∏–ª—å—Ç—Ä—É–µ–º –¥–∞–Ω–Ω—ã–µ –∏ —Å–æ–∑–¥–∞–µ–º –∫–æ–ø–∏—é
    product_data = prices[prices['product'] == product].copy()

    # –ê–Ω–∞–ª–∏–∑
    latest_price = product_data['price'].tolist()[-1]    
    difference = current_price - latest_price
    percent_diff = (difference / latest_price) * 100

    st.write(f'–†–∞–∑–Ω–∏—Ü–∞ –≤ —Ü–µ–Ω–∞—Ö —Å–æ—Å—Ç–∞–≤–∏–ª–∞: {round(difference, 2)}')

    # –°–æ–∑–¥–∞–µ–º –≥—Ä–∞—Ñ–∏–∫ Plotly
    fig = go.Figure()

    # –î–æ–±–∞–≤–ª—è–µ–º –∏—Å—Ç–æ—Ä–∏—á–µ—Å–∫–∏–µ –¥–∞–Ω–Ω—ã–µ
    fig.add_trace(go.Scatter(
        x=product_data['date'],
        y=list(product_data['price']),
        mode='lines+markers',
        name=product,
        line=dict(color='blue', width=3),
        marker=dict(size=8),
        hovertemplate='<b>%{x|%d.%m.%Y}</b><br>–¶–µ–Ω–∞: %{y} —Ä—É–±<extra></extra>'
    ))

    # –î–æ–±–∞–≤–ª—è–µ–º –ª–∏–Ω–∏—é —Ç–µ–∫—É—â–µ–π —Ü–µ–Ω—ã
    fig.add_hline(
        y=current_price,
        line_dash="dash",
        line_color="red",
        annotation_text=f"–í–∞—à–∞ —Ü–µ–Ω–∞: {current_price} —Ä—É–±"
    )

    # –ù–∞—Å—Ç—Ä–æ–π–∫–∏ –≥—Ä–∞—Ñ–∏–∫–∞
    fig.update_layout(
        title=f'–°—Ä–∞–≤–Ω–µ–Ω–∏–µ —Ü–µ–Ω: {product}',
        xaxis_title='–î–∞—Ç–∞',
        yaxis_title='–¶–µ–Ω–∞ (—Ä—É–±)',
        height=500,
        yaxis=dict(
            range=[min(product_data['price'].min() * 0.5, current_price * 0.5), max(product_data['price'].max() * 1.1, current_price * 1.1)]
        )
    )
        
    st.plotly_chart(fig, use_container_width=True)
    

# –ó–∞–ø—É—Å–∫
plotly_price_calculator()