# –ö–æ–º–ø–∞–∫—Ç–Ω–∞—è –≤–µ—Ä—Å–∏—è
IMAGE_FOLDER = "images/slider"

def main():
    st.header("–°—Ç–∏—Ö–∏—è –∫—Ä–∞—Å–æ—Ç—ã")
    
    if not os.path.exists(IMAGE_FOLDER):
        st.error(f"–ü–∞–ø–∫–∞ '{IMAGE_FOLDER}' –Ω–µ –Ω–∞–π–¥–µ–Ω–∞!")
        return
    
    # –ó–∞–≥—Ä—É–∑–∫–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π
    images = []
    for file in sorted(os.listdir(IMAGE_FOLDER)):
        if file.lower().endswith(('.jpg', '.jpeg', '.png', '.gif')):
            img_path = os.path.join(IMAGE_FOLDER, file)
            images.append((file, Image.open(img_path)))
    
    if not images:
        st.error("–ò–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –Ω–µ –Ω–∞–π–¥–µ–Ω—ã")
        return
    
    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è
    if 'img_index' not in st.session_state:
        st.session_state.img_index = 0
    
    idx = st.session_state.img_index
    name, img = images[idx]

    st.image(img, use_column_width=True)
    
    # –£–ø—Ä–∞–≤–ª–µ–Ω–∏–µ
    col1, col2, col3 = st.columns(3)
    
    with col2:
        if st.button("–ù–∞–∑–∞–¥", use_container_width=True) and idx > 0:
            st.session_state.img_index -= 1
            st.rerun()
    
    with col1:
        st.write(f"**{idx + 1} / {len(images)}**")
    
    with col3:
        if st.button("–í–ø–µ—Ä–µ–¥", use_container_width=True) and idx < len(images) - 1:
            st.session_state.img_index += 1
            st.rerun()
    

if __name__ == "__main__":
    main()